def compute_class_weights(df, num_classes=4, img_size=512, n_sample=200):
    counts  = np.zeros(num_classes, dtype=np.float64)
    skipped = 0

    originals = df[df["augmented"] == False]
    pool      = originals if len(originals) >= n_sample else df

    sample = pool.sample(min(n_sample, len(pool)), random_state=RANDOM_STATE)

    for _, row in tqdm(sample.iterrows(), total=len(sample),
                       desc="Computing class weights"):
        if not os.path.exists(row["mask_path"]):
            skipped += 1
            continue

        mask = cv2.imread(row["mask_path"], 0)
        if mask is None:
            skipped += 1
            continue

        mask     = cv2.resize(mask, (img_size, img_size),
                              interpolation=cv2.INTER_NEAREST)
        # ← UPDATED: class-conditional threshold to match SegmentationDataset exactly
        class_id = int(row["class_id"])
        thresh   = BS_MASK_THRESHOLD if class_id == 2 else MASK_THRESHOLD
        mask     = (mask >= thresh).astype(np.uint8)
        mask     = np.where(mask == 1, class_id, 0)

        for c in range(num_classes):
            counts[c] += (mask == c).sum()

    if skipped > 0:
        print(f"Skipped {skipped}/{len(sample)} masks (unreadable)")

    if counts.sum() == 0:
        print("No masks read — using uniform weights")
        return torch.ones(num_classes, dtype=torch.float32).to(DEVICE)

    weights = 1.0 / (counts + 1)
    weights = weights / weights.sum() * num_classes

    labels = ["background", "bacterial", "brown_spot", "nutrient"]
    print("\nPixel counts & weights:")
    for c, (cnt, w) in enumerate(zip(counts, weights)):
        print(f"  [{c}] {labels[c]:12s}: {int(cnt):>12,} px  weight={w:.4f}")

    return torch.tensor(weights, dtype=torch.float32).to(DEVICE)


