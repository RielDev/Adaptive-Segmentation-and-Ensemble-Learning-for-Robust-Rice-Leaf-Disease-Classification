import torch.nn.functional as F

def compute_dice_iou(all_preds, all_targets, num_classes=4):
    dice_scores = []
    iou_scores  = []

    for cls in range(1, num_classes):
        pred_c = (all_preds   == cls)
        tgt_c  = (all_targets == cls)

        if tgt_c.sum() == 0:
            continue

        inter = (pred_c & tgt_c).sum().item()
        union = pred_c.sum().item() + tgt_c.sum().item()

        dice_scores.append((2 * inter) / (union + 1e-7))
        iou_scores.append(inter / (union - inter + 1e-7))

    return (
        np.mean(dice_scores) if dice_scores else 0.0,
        np.mean(iou_scores)  if iou_scores  else 0.0
    )


def per_class_metrics(all_preds, all_targets, num_classes=4):
    results = {}
    for cls in range(1, num_classes):
        pred_c = (all_preds   == cls)
        tgt_c  = (all_targets == cls)

        if tgt_c.sum() == 0:
            results[cls] = (0.0, 0.0)
            continue

        inter = (pred_c & tgt_c).sum().item()
        union = pred_c.sum().item() + tgt_c.sum().item()

        results[cls] = (
            (2 * inter) / (union + 1e-7),
            inter / (union - inter + 1e-7)
        )
    return results


@torch.no_grad()
def tta_predict(model, images):
    """
    Fast 4-pass TTA used during training validation.
    Kept lightweight so validation does not slow down the training loop.
    """
    pred_orig = torch.softmax(model(images), dim=1)
    pred_hf   = torch.softmax(model(torch.flip(images, [-1])), dim=1).flip(-1)
    pred_vf   = torch.softmax(model(torch.flip(images, [-2])), dim=1).flip(-2)
    pred_r90  = torch.softmax(
        model(torch.rot90(images, 1, [-2, -1])), dim=1
    )
    pred_r90  = torch.rot90(pred_r90, -1, [-2, -1])
    return (pred_orig + pred_hf + pred_vf + pred_r90) / 4.0


@torch.no_grad()
def tta_predict_multiscale(model, images):
    """
    12-pass multi-scale TTA used only at final test evaluation.
    Runs 4 geometric augmentations (orig, hflip, vflip, rot90) at 3 scales
    (448, 512, 576) then averages all probability maps back at the native
    512×512 resolution.

    Brown Spot benefits most from this: the model sees lesions at a slightly
    larger relative scale (448 input → lesion appears larger) and smaller
    relative scale (576 → more spatial context), which smooths out the
    confidence map over lesion borders.
    """
    H, W   = images.shape[2], images.shape[3]
    scales = [448, 512, 576]
    preds  = []

    for s in scales:
        imgs_s = F.interpolate(
            images, size=(s, s), mode='bilinear', align_corners=False
        )

        # Original
        p = torch.softmax(model(imgs_s), dim=1)
        preds.append(F.interpolate(p, size=(H, W), mode='bilinear', align_corners=False))

        # Horizontal flip
        p = torch.softmax(model(torch.flip(imgs_s, [-1])), dim=1).flip(-1)
        preds.append(F.interpolate(p, size=(H, W), mode='bilinear', align_corners=False))

        # Vertical flip
        p = torch.softmax(model(torch.flip(imgs_s, [-2])), dim=1).flip(-2)
        preds.append(F.interpolate(p, size=(H, W), mode='bilinear', align_corners=False))

        # 90-degree rotation
        p     = torch.softmax(model(torch.rot90(imgs_s, 1, [-2, -1])), dim=1)
        p     = torch.rot90(p, -1, [-2, -1])
        preds.append(F.interpolate(p, size=(H, W), mode='bilinear', align_corners=False))

    return sum(preds) / len(preds)   # mean over all 12 passes


