class SegmentationDataset(Dataset):

    def __init__(self, df, transform=None, brown_spot_transform=None, img_size=512):
        self.df                   = df.reset_index(drop=True)
        self.transform            = transform
        self.brown_spot_transform = brown_spot_transform
        self.img_size             = img_size
        self._warned              = set()

    def __len__(self):
        return len(self.df)

    def _make_zeros(self):
        img  = torch.zeros(3, self.img_size, self.img_size, dtype=torch.float32)
        mask = torch.zeros(self.img_size, self.img_size, dtype=torch.long)
        return img, mask

    def __getitem__(self, idx):
        row = self.df.iloc[idx]

        image = cv2.imread(row["image_path"])
        if image is None:
            if row["image_path"] not in self._warned:
                print(f"\nCannot read image: {row['image_path']}")
                self._warned.add(row["image_path"])
            return self._make_zeros()

        image = cv2.cvtColor(image, cv2.COLOR_BGR2RGB)

        mask = cv2.imread(row["mask_path"], 0)
        if mask is None:
            if row["mask_path"] not in self._warned:
                print(f"\nCannot read mask : {row['mask_path']}")
                self._warned.add(row["mask_path"])
            return self._make_zeros()

        image = cv2.resize(image, (self.img_size, self.img_size))
        mask  = cv2.resize(mask,  (self.img_size, self.img_size),
                           interpolation=cv2.INTER_NEAREST)

        class_id = int(row["class_id"])
        # ← UPDATED: class-conditional threshold
        # Brown Spot lesion borders are soft and JPEG-compressed masks lose
        # the outer ring at threshold=200; lower threshold recovers those pixels.
        thresh = BS_MASK_THRESHOLD if class_id == 2 else MASK_THRESHOLD
        mask   = (mask >= thresh).astype(np.uint8)
        mask   = np.where(mask == 1, class_id, 0).astype(np.uint8)

        # Use gentler augmentation for Brown Spot
        is_brown_spot = (class_id == 2)
        tfm = (
            self.brown_spot_transform
            if is_brown_spot and self.brown_spot_transform
            else self.transform
        )

        if tfm:
            transformed = tfm(image=image, mask=mask)
            image = transformed["image"]
            mask  = transformed["mask"]

        return image, mask.long()

