def boundary_loss_fn(pred, target, num_classes=4):
    loss      = 0.0
    pred_soft = torch.softmax(pred, dim=1)
    for c in range(1, num_classes):
        tgt      = (target == c).float().unsqueeze(1)
        dilated  = F.max_pool2d(tgt,  kernel_size=3, stride=1, padding=1) > 0.5
        eroded   = -F.max_pool2d(-tgt, kernel_size=3, stride=1, padding=1) > 0.5
        boundary = (dilated & ~eroded).float()
        error    = torch.abs(pred_soft[:, c:c+1] - tgt)
        loss    += (error * boundary).mean()
    return loss / (num_classes - 1)

def combined_loss(pred, target):
    base = (
        0.20 * tversky_loss(pred, target)
        + 0.25 * focal_loss(pred, target)
        + 0.25 * lovasz_loss(pred, target)
        + 0.20 * iou_loss(pred, target)
        + 0.10 * boundary_loss_fn(pred, target)
    )
    # Brown Spot FN penalty — uses raw logit for class 2 directly.
    # ← FIXED: was F.binary_cross_entropy(softmax_prob, ...) which is unsafe
    #   inside torch.amp.autocast — autocast can produce float16 tensors and
    #   BCE requires float32, causing a RuntimeError.
    #   F.binary_cross_entropy_with_logits operates on the raw logit instead,
    #   which is both autocast-safe and numerically more stable (no softmax
    #   followed by log — the two operations are fused internally).
    bs_logit      = pred[:, 2]                    # (B, H, W) raw logit, no softmax
    bs_tgt        = (target == 2).float()         # (B, H, W) binary ground truth
    bs_fn_penalty = F.binary_cross_entropy_with_logits(
        bs_logit, bs_tgt, reduction='mean'
    )
    return base + 0.10 * bs_fn_penalty

