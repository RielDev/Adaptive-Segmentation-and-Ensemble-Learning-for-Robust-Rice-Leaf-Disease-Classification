# ── Connected component filter ────────────────────────────────────────────────
from skimage import measure

def filter_small_components(pred_tensor, thresholds=None):
    if thresholds is None:
        thresholds = {1: 200, 2: 30, 3: 150}
    result = pred_tensor.clone()
    for i in range(len(pred_tensor)):
        arr = result[i].numpy().copy()
        for class_id, min_px in thresholds.items():
            cls_mask = (arr == class_id)
            if cls_mask.sum() == 0:
                continue
            labeled = measure.label(cls_mask)
            for region in measure.regionprops(labeled):
                if region.area < min_px:
                    arr[labeled == region.label] = 0
        result[i] = torch.from_numpy(arr)
    return result

