"""**Augmentation:**

**Augmentation target** = 750

**Transforms Applied:**

1. Horizontal flip
2. Small rotation ±10°
3. Brightness / Contrast jitter ±15 %
4. Gentle Gaussian blur kernel 3×3
"""

AUG_TARGET   = 750
AUG_SEED     = RANDOM_STATE

rng = np.random.default_rng(AUG_SEED)

def aug_hflip(img, mask=None):
    img_out = cv2.flip(img, 1)
    mask_out = cv2.flip(mask, 1) if mask is not None else None
    return img_out, mask_out

def aug_rotate(img, mask=None, angle_deg=8):
    h, w = img.shape[:2]
    M = cv2.getRotationMatrix2D((w / 2, h / 2), angle_deg, 1.0)
    img_out  = cv2.warpAffine(img,  M, (w, h), flags=cv2.INTER_LINEAR,
                               borderMode=cv2.BORDER_REFLECT_101)
    if mask is not None:
        mask_out = cv2.warpAffine(mask, M, (w, h), flags=cv2.INTER_NEAREST,
                                  borderMode=cv2.BORDER_REFLECT_101)
    else:
        mask_out = None
    return img_out, mask_out

def aug_brightness(img, mask=None, alpha=1.12, beta=10):
    img_out  = np.clip(img.astype(np.float32) * alpha + beta, 0, 255).astype(np.uint8)
    mask_out = mask
    return img_out, mask_out

def aug_blur(img, mask=None):
    img_out  = cv2.GaussianBlur(img, (3, 3), 0)
    mask_out = mask
    return img_out, mask_out

TRANSFORMS = {
    "hflip":      (aug_hflip,       {}),
    "rotate_cw":  (aug_rotate,      {"angle_deg":  8}),
    "rotate_ccw": (aug_rotate,      {"angle_deg": -8}),
    "brightness": (aug_brightness,  {"alpha": 1.12, "beta":  10}),
    "dim":        (aug_brightness,  {"alpha": 0.88, "beta": -10}),
    "blur":       (aug_blur,        {}),
}

aug_records = []

counts = df["class_name"].value_counts().to_dict()

for class_name, current_count in sorted(counts.items()):
    needed = AUG_TARGET - current_count
    if needed <= 0:
        print(f"  ⏭️  {class_name}: {current_count} samples — at/above target, skipping")
        continue

    print(f"\nAugmenting {class_name}: {current_count} → {AUG_TARGET}  (+{needed})")

    class_aug_dir      = os.path.join(AUG_DIR, class_name)
    class_aug_mask_dir = os.path.join(AUG_DIR, class_name, "masks")
    os.makedirs(class_aug_dir,      exist_ok=True)
    os.makedirs(class_aug_mask_dir, exist_ok=True)

    cls_rows = df[df["class_name"] == class_name].reset_index(drop=True)
    class_id = int(cls_rows["class_id"].iloc[0])

    transform_names = list(TRANSFORMS.keys())
    generated = 0

    for t_idx, t_name in enumerate(
        [transform_names[i % len(transform_names)] for i in range(needed)]
    ):
        src_row = cls_rows.iloc[t_idx % len(cls_rows)]

        img = cv2.imread(src_row["image_path"])
        if img is None:
            continue

        mask = None
        if src_row["mask_path"]:
            mask = cv2.imread(src_row["mask_path"], 0)

        fn, kwargs = TRANSFORMS[t_name]
        aug_img, aug_mask = fn(img, mask, **kwargs)

        src_stem = Path(src_row["image_path"]).stem
        aug_img_fname  = f"aug_{t_name}_{src_stem}.jpg"
        aug_img_path   = os.path.join(class_aug_dir, aug_img_fname)
        cv2.imwrite(aug_img_path, aug_img, [cv2.IMWRITE_JPEG_QUALITY, 95])

        aug_mask_path = ""
        if aug_mask is not None:
            aug_mask_fname = f"aug_{t_name}_{src_stem}_mask.png"
            aug_mask_path  = os.path.join(class_aug_mask_dir, aug_mask_fname)
            cv2.imwrite(aug_mask_path, aug_mask)

        aug_records.append({
            "image_path": aug_img_path,
            "mask_path":  aug_mask_path,
            "class_name": class_name,
            "class_id":   class_id,
            "subfolder":  "augmented",
            "augmented":  True,
        })
        generated += 1

