def build_mask_stem(img_stem):
    parts = img_stem.rsplit("_", 1)
    if len(parts) != 2 or not parts[1].isdigit():
        print(f"Unexpected filename (skipped): {img_stem}")
        return None
    base, idx = parts
    return f"{base}_mask_{idx}"


def find_mask_file(mask_dir, mask_stem):
    for ext in [".png", ".jpg", ".jpeg"]:
        p = Path(mask_dir) / f"{mask_stem}{ext}"
        if p.exists():
            return str(p)
    return None

"""Validates that:

*   Both files are readable
*   Mask is non-empty
"""

def validate_pair(img_path, mask_path):
    img = cv2.imread(img_path)
    if img is None:
        return False, "unreadable_image"

    mask = cv2.imread(mask_path, 0)
    if mask is None:
        return False, "unreadable_mask"

    if mask.max() == 0:
        return False, "empty_mask"

    if mask.shape[0] < 32 or mask.shape[1] < 32:
        return False, f"mask_too_small_{mask.shape}"

    return True, "ok"

"""Validation for classes with no masks (healthy)"""

def validate_image_only(img_path):
    img = cv2.imread(img_path)
    if img is None:
        return False, "unreadable_image"
    if img.shape[0] < 32 or img.shape[1] < 32:
        return False, f"image_too_small_{img.shape}"
    return True, "ok"

