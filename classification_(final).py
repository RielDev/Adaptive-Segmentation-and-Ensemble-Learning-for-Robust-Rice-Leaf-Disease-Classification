"""**Metrics:**"""

def compute_metrics(all_preds, all_labels, print_cm=False):
    preds  = np.array(all_preds)
    labels = np.array(all_labels)

    mask   = labels != -1
    preds  = preds[mask]
    labels = labels[mask]

    if len(labels) == 0:
        return {"accuracy": 0, "precision": 0, "recall": 0, "f1": 0,
                "fpr_nutrient_def": 0, "per_class_f1": {},
                "confusion_matrix": np.zeros((4, 4))}

    acc    = accuracy_score(labels, preds)
    prec   = precision_score(labels, preds, average="weighted", zero_division=0)
    rec    = recall_score(labels,    preds, average="weighted", zero_division=0)
    f1     = f1_score(labels,        preds, average="weighted", zero_division=0)
    f1_per = f1_score(labels,        preds, average=None,       zero_division=0)

    cm     = confusion_matrix(labels, preds, labels=list(range(NUM_CLASSES)))
    nd_idx = NUTRIENT_DEF_IDX
    fp_nd  = cm[:, nd_idx].sum() - cm[nd_idx, nd_idx]
    tn_nd  = (cm.sum() - cm[nd_idx, :].sum()
              - cm[:, nd_idx].sum() + cm[nd_idx, nd_idx])
    fpr_nd = float(fp_nd) / float(fp_nd + tn_nd) if (fp_nd + tn_nd) > 0 else 0.0

    if print_cm:
        print("\nConfusion Matrix:")
        print(pd.DataFrame(
            cm,
            index=[f"True: {CLASS_NAMES[i]}"   for i in range(NUM_CLASSES)],
            columns=[f"Pred: {CLASS_NAMES[i]}" for i in range(NUM_CLASSES)]
        ).to_string())

    return {
        "accuracy":         acc,
        "precision":        prec,
        "recall":           rec,
        "f1":               f1,
        "fpr_nutrient_def": fpr_nd,
        "per_class_f1": {CLASS_NAMES[i]: round(float(v), 4)
                         for i, v in enumerate(f1_per)},
        "confusion_matrix": cm,
        "all_preds":        preds,
        "all_labels":       labels,
    }

print("✅ compute_metrics() ready")

"""**Training Loop:**"""

def train_model(model, optimizer, scheduler, train_loader, val_loader,
                model_name, epochs=EPOCHS, patience=PATIENCE,
                save_dir=MODELS_DIR, use_focal_loss=True,
                gamma=2.0, label_smoothing=0.05, use_mixup=True,
                mixup_alpha=0.2, swa_start=None, class_weights=None):

    if use_focal_loss:
        criterion = FocalLoss(
            gamma=gamma,
            label_smoothing=label_smoothing,
            num_classes=NUM_CLASSES,
            class_weights=class_weights,
        )
        cw_str = ("custom" if class_weights is not None else "uniform")
        print(f"Using Focal Loss (gamma={gamma}, smoothing={label_smoothing}, "
              f"class_weights={cw_str})")
    else:
        criterion = nn.CrossEntropyLoss(label_smoothing=label_smoothing)
        print(f"Using CrossEntropyLoss (smoothing={label_smoothing})")

    # FIX: consistent val criterion — plain CE, always, regardless of use_mixup
    val_criterion = nn.CrossEntropyLoss(label_smoothing=label_smoothing)

    scaler    = torch.amp.GradScaler('cuda')
    save_path = os.path.join(save_dir, f"best_{model_name}.pth")

    if swa_start is not None and swa_start < epochs:
        from torch.optim.swa_utils import AveragedModel, update_bn
        swa_model = AveragedModel(model)
        print(f"SWA enabled — starting at epoch {swa_start + 1}")
    else:
        swa_model = None
        swa_start = epochs + 1

    best_f1          = 0.0
    patience_counter = 0

    history = {
        "train_loss": [], "train_acc": [],
        "val_loss":   [], "val_acc":   [],
        "val_f1":     [], "val_fpr_nd": [], "lr": [],
    }

    print(f"\n{'='*60}")
    print(f"  Training: {model_name}")
    print(f"{'='*60}")

    for epoch in range(epochs):

        # ── TRAIN ───────────────────────────────────────────
        model.train()
        t_losses, t_preds, t_labels = [], [], []

        for images, labels in tqdm(train_loader,
                                   desc=f"Train {epoch+1}/{epochs}",
                                   leave=False):
            images = images.to(DEVICE)
            labels = labels.to(DEVICE)
            valid  = labels != -1
            if valid.sum() == 0:
                continue
            images = images[valid]
            labels = labels[valid]

            optimizer.zero_grad(set_to_none=True)

            with torch.amp.autocast("cuda"):
                if use_mixup and epoch >= 2:
                    mixed_images, y_a, y_b, lam = mixup_batch(
                        images, labels, alpha=mixup_alpha)
                    outputs = model(mixed_images)
                    loss    = mixup_criterion(criterion, outputs, y_a, y_b, lam)
                    preds   = torch.argmax(outputs, 1).cpu().numpy()
                    true_labels = y_a if lam >= 0.5 else y_b
                    t_preds.extend(preds)
                    t_labels.extend(true_labels.cpu().numpy())
                else:
                    outputs = model(images)
                    loss    = criterion(outputs, labels)
                    t_preds.extend(torch.argmax(outputs, 1).cpu().numpy())
                    t_labels.extend(labels.cpu().numpy())

                t_losses.append(loss.item())

            scaler.scale(loss).backward()
            scaler.unscale_(optimizer)
            torch.nn.utils.clip_grad_norm_(model.parameters(), max_norm=1.0)
            scaler.step(optimizer)
            scaler.update()

        scheduler.step()
        current_lr = optimizer.param_groups[-1]["lr"]

        train_loss = np.mean(t_losses)
        train_acc  = accuracy_score(t_labels, t_preds)

        # ── VALIDATE ────────────────────────────────────────
        model.eval()
        v_losses, v_preds, v_labels = [], [], []

        with torch.no_grad():
            for images, labels in val_loader:
                images = images.to(DEVICE)
                labels = labels.to(DEVICE)
                valid  = labels != -1
                if valid.sum() == 0:
                    continue
                images = images[valid]
                labels = labels[valid]
                with torch.amp.autocast("cuda"):
                    outputs = model(images)
                    loss = val_criterion(outputs, labels)
                v_losses.append(loss.item())
                v_preds.extend(torch.argmax(outputs, 1).cpu().numpy())
                v_labels.extend(labels.cpu().numpy())

        val_loss    = np.mean(v_losses)
        val_metrics = compute_metrics(v_preds, v_labels)
        val_acc     = val_metrics["accuracy"]
        val_f1      = val_metrics["f1"]
        val_fpr_nd  = val_metrics["fpr_nutrient_def"]

        history["train_loss"].append(train_loss)
        history["train_acc"].append(train_acc)
        history["val_loss"].append(val_loss)
        history["val_acc"].append(val_acc)
        history["val_f1"].append(val_f1)
        history["val_fpr_nd"].append(val_fpr_nd)
        history["lr"].append(current_lr)

        if swa_model is not None and epoch >= swa_start:
            swa_model.update_parameters(model)

        print(f"  Ep {epoch+1:>3}/{epochs}  LR={current_lr:.2e}"
              f"  Loss={train_loss:.4f}/{val_loss:.4f}"
              f"  Acc={train_acc:.4f}/{val_acc:.4f}"
              f"  F1={val_f1:.4f}"
              f"  FPR_ND={val_fpr_nd:.4f}", end="")

        if val_f1 > best_f1:
            best_f1          = val_f1
            patience_counter = 0
            torch.save({
                "epoch":                epoch + 1,
                "model_state_dict":     model.state_dict(),
                "optimizer_state_dict": optimizer.state_dict(),
                "scheduler_state_dict": scheduler.state_dict(),
                "best_f1":              best_f1,
            }, save_path)
            print(f"  💾 best={best_f1:.4f}")
        else:
            patience_counter += 1
            print(f"  ({patience_counter}/{patience})")
            if patience_counter >= patience:
                print(f"\n  ⏹  Early stopping at epoch {epoch+1}")
                break

    if swa_model is not None and epoch >= swa_start:
        print("\n  📊 Updating SWA batch normalization statistics...")
        update_bn(train_loader, swa_model, device=DEVICE)
        model = swa_model
        print("  ✅ SWA model ready for evaluation")

    print(f"\n✅ {model_name} done — Best Val F1: {best_f1:.4f}")
