class ClassificationDataset(Dataset):

    def __init__(self, df, transform=None, use_mixup=False):
        self.df        = df.reset_index(drop=True)
        self.transform = transform
        self.use_mixup = use_mixup
        self._warned   = set()

    def __len__(self):
        return len(self.df)

    def __getitem__(self, idx):
        row   = self.df.iloc[idx]
        label = int(row["label"])

        image = cv2.imread(row["image_path"])
        if image is None:
            if row["image_path"] not in self._warned:
                print(f"\n⚠️  Unreadable at runtime: {row['image_path']}")
                self._warned.add(row["image_path"])
            return torch.zeros(3, IMG_SIZE, IMG_SIZE, dtype=torch.float32), -1

        image = cv2.cvtColor(image, cv2.COLOR_BGR2RGB)
        if self.transform:
            image = self.transform(image)
        return image, label

