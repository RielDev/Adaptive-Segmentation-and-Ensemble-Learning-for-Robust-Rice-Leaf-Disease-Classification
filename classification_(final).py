def _param_groups(model, head_params, lr):
    head_ids    = {id(p) for p in head_params}
    body_params = [p for p in model.parameters() if id(p) not in head_ids]
    return [
        {"params": body_params, "lr": lr * 0.05},
        {"params": head_params, "lr": lr},
    ]


def build_mobilenetv3(num_classes=NUM_CLASSES, lr=LR):
    model = tv_models.mobilenet_v3_large(
        weights=tv_models.MobileNet_V3_Large_Weights.IMAGENET1K_V2)
    in_features = model.classifier[-1].in_features
    model.classifier[-1] = nn.Linear(in_features, num_classes)
    model = model.to(DEVICE)
    head_params = list(model.classifier.parameters())
    optimizer   = torch.optim.AdamW(
        _param_groups(model, head_params, lr), weight_decay=1e-4)
    total, trainable = count_params(model)
    print(f"MobileNetV3-Large  |  {total:,} total params  |  {trainable:,} trainable")
    return model, optimizer


def build_nasnet_mobile(num_classes=NUM_CLASSES, lr=LR):
    """
    NASNet-Mobile via pretrainedmodels.
    Weights are downloaded once from the Wayback Machine archive and cached
    at ~/.cache/torch/hub/checkpoints to survive Colab session restarts.
    Early conv layers are frozen to prevent catastrophic forgetting on this
    small dataset; the rest uses differential LR fine-tuning.
    """
    import subprocess

    ckpt_dir  = os.path.expanduser("~/.cache/torch/hub/checkpoints")
    ckpt_file = os.path.join(ckpt_dir, "nasnetamobile-7e03cead.pth")

    os.makedirs(ckpt_dir, exist_ok=True)

    if not os.path.exists(ckpt_file):
        url = (
            "https://web.archive.org/web/2024/http://data.lip6.fr/cadene/pretrainedmodels/"
            "nasnetamobile-7e03cead.pth"
        )
        print("Downloading NASNet-Mobile weights...")
        subprocess.run(
            ["wget", "--no-check-certificate", "-q", "-O", ckpt_file, url],
            check=True
        )
        print("✅ Download complete.")
    else:
        print("✅ NASNet-Mobile weights already cached.")

    model = pretrainedmodels.__dict__["nasnetamobile"](
        num_classes=1000, pretrained="imagenet")

    in_features = model.last_linear.in_features
    model.last_linear = nn.Sequential(
        nn.Dropout(0.2),
        nn.Linear(in_features, num_classes)
    )

    # Freeze early conv layers — NASNet overfits badly if fully unfrozen immediately
    freeze_prefixes = ["conv0"]
    for name, param in model.named_parameters():
        if any(name.startswith(p) for p in freeze_prefixes):
            param.requires_grad = False

    model = model.to(DEVICE)

    head_params = list(model.last_linear.parameters())
    head_ids    = {id(p) for p in head_params}
    body_params = [p for p in model.parameters()
                   if p.requires_grad and id(p) not in head_ids]

    optimizer = torch.optim.AdamW([
        {"params": body_params, "lr": lr * 0.05, "weight_decay": 3e-4},
        {"params": head_params, "lr": lr,         "weight_decay": 5e-4},
    ])

    total, trainable = count_params(model)
    print(f"NASNet-Mobile  |  {total:,} total params  |  {trainable:,} trainable")
    return model, optimizer


def build_efficientnet_lite(num_classes=NUM_CLASSES, lr=LR,
                             dropout_rate=0.2, freeze_body=False):
    """
    dropout_rate default lowered to 0.2 (was 0.3).
    MixUp already provides strong regularisation; stacking 0.3 dropout
    on top was over-regularising on this small dataset.
    """
    model = timm.create_model("efficientnet_lite0", pretrained=True,
                               num_classes=num_classes, drop_rate=dropout_rate)
    model = model.to(DEVICE)

    if freeze_body:
        for param in model.parameters():
            param.requires_grad = False
        for param in model.classifier.parameters():
            param.requires_grad = True
        head_params = list(model.classifier.parameters())
        optimizer   = torch.optim.AdamW(head_params, lr=lr, weight_decay=5e-4)
    else:
        head_params = list(model.classifier.parameters())
        optimizer   = torch.optim.AdamW(
            _param_groups(model, head_params, lr), weight_decay=5e-4)

    total, trainable = count_params(model)
    frozen = "(body frozen)" if freeze_body else ""
    print(f"EfficientNet-Lite0  |  {total:,} total  |  {trainable:,} trainable {frozen}")
    return model, optimizer


def count_params(model):
    total     = sum(p.numel() for p in model.parameters())
    trainable = sum(p.numel() for p in model.parameters() if p.requires_grad)
    return total, trainable


def cosine_with_warmup_scheduler(optimizer, warmup_epochs, total_epochs,
                                  min_lr_ratio=1e-3):
    def fn(epoch):
        if epoch < warmup_epochs:
            return (epoch + 1) / warmup_epochs
        progress = (epoch - warmup_epochs) / max(1, total_epochs - warmup_epochs)
        cosine   = 0.5 * (1.0 + math.cos(math.pi * progress))
        return max(cosine * (1.0 - min_lr_ratio) + min_lr_ratio, min_lr_ratio)
    return torch.optim.lr_scheduler.LambdaLR(optimizer, lr_lambda=fn)

