def make_loaders(batch_size=BATCH_SIZE, weighted_oversample=True):
    train_ds = ClassificationDataset(train_df, transform=train_transform)
    val_ds   = ClassificationDataset(val_df,   transform=val_transform)
    test_ds  = ClassificationDataset(test_df,  transform=val_transform)

    kw = dict(num_workers=NUM_WORKERS, pin_memory=True)

    if weighted_oversample:
        labels        = train_df["label"].values
        class_counts  = np.bincount(labels, minlength=NUM_CLASSES)
        class_weights = 1.0 / np.maximum(class_counts, 1)
        sample_weights = class_weights[labels]
        sampler = torch.utils.data.WeightedRandomSampler(
            weights=torch.from_numpy(sample_weights).float(),
            num_samples=len(train_ds),
            replacement=True,
        )
        train_loader = DataLoader(train_ds, batch_size=batch_size,
                                  sampler=sampler, **kw)
        print("Train loader : WeightedRandomSampler (oversampling minority classes)")
        for c, cnt in enumerate(class_counts):
            print(f"  class {c} ({CLASS_NAMES[c]:<25}): {cnt} samples")
    else:
        train_loader = DataLoader(train_ds, batch_size=batch_size,
                                  shuffle=True, **kw)

    val_loader  = DataLoader(val_ds,  batch_size=batch_size, shuffle=False, **kw)
    test_loader = DataLoader(test_ds, batch_size=batch_size, shuffle=False, **kw)

    print(f"Train batches : {len(train_loader)}")
    print(f"Val batches   : {len(val_loader)}")
    print(f"Test batches  : {len(test_loader)}")
    return train_loader, val_loader, test_loader

