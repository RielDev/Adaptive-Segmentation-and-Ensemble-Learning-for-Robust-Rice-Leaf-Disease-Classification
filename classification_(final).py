class FocalLoss(nn.Module):
    def __init__(self, gamma=1.0, label_smoothing=0.05,
                 num_classes=4, class_weights=None):
        super().__init__()
        self.gamma           = gamma
        self.label_smoothing = label_smoothing
        self.num_classes     = num_classes
        self.register_buffer(
            'class_weights',
            class_weights if class_weights is not None
            else torch.ones(num_classes)
        )

    def forward(self, logits, targets):
        with torch.no_grad():
            soft = torch.full_like(
                logits, self.label_smoothing / (self.num_classes - 1))
            soft.scatter_(1, targets.unsqueeze(1), 1.0 - self.label_smoothing)

        log_probs = F.log_softmax(logits, dim=1)
        probs     = log_probs.exp()
        p_t       = (probs * soft).sum(dim=1)
        weight    = (1.0 - p_t) ** self.gamma

        # FIX: move class_weights to same device as targets before indexing
        sample_weights = self.class_weights.to(targets.device)[targets]

        loss = -(soft * log_probs).sum(dim=1)
        return (weight * sample_weights * loss).mean()

