def plot_history(history, model_name, meta_dir=META_DIR):
    n = len(history["train_loss"])
    x = range(1, n + 1)

    fig, axes = plt.subplots(1, 4, figsize=(20, 4))
    fig.suptitle(f"Training History — {model_name}", fontsize=13)

    axes[0].plot(x, history["train_loss"], label="Train", color="steelblue")
    axes[0].plot(x, history["val_loss"],   label="Val",   color="darkorange")
    axes[0].set_title("Loss"); axes[0].set_xlabel("Epoch")
    axes[0].legend(); axes[0].grid(True)

    axes[1].plot(x, history["train_acc"], label="Train Acc", color="steelblue")
    axes[1].plot(x, history["val_acc"],   label="Val Acc",   color="darkorange")
    axes[1].plot(x, history["val_f1"],    label="Val F1",    color="green",
                 linestyle="--")
    axes[1].axhline(0.90, color="red", linestyle=":", alpha=0.6, label="Target 0.90")
    axes[1].set_title("Accuracy / F1"); axes[1].set_xlabel("Epoch")
    axes[1].legend(fontsize=8); axes[1].grid(True)

    axes[2].plot(x, history["val_fpr_nd"], color="crimson",
                 label="FPR — Nutrient Def")
    axes[2].set_title("Val FPR — Nutrient Deficiency")
    axes[2].set_xlabel("Epoch"); axes[2].set_ylim(0, 1)
    axes[2].legend(fontsize=8); axes[2].grid(True)

    axes[3].plot(x, history["lr"], color="purple")
    axes[3].set_title("Learning Rate (head)")
    axes[3].set_xlabel("Epoch")
    axes[3].set_yscale("log"); axes[3].grid(True)

    plt.tight_layout()
    fname = os.path.join(
        meta_dir,
        f"training_curves_{model_name.lower().replace(' ', '_')}.png")
    plt.savefig(fname, dpi=120)
    plt.show()
