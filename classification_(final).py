"""**Evaluation:**"""

def predict_with_thresholds(model, loader, thresholds=None, device=DEVICE):
    if thresholds is None:
        thresholds = [0.25] * NUM_CLASSES
    model.eval()
    all_preds, all_labels = [], []
    with torch.no_grad():
        for images, labels in tqdm(loader, desc="Threshold prediction", leave=False):
            images = images.to(device)
            labels = labels.numpy()
            valid  = labels != -1
            if valid.sum() == 0:
                continue
            images = images[valid]
            probs  = torch.softmax(model(images), dim=1).cpu().numpy()
            scaled = probs / np.array(thresholds)
            preds  = scaled.argmax(axis=1)
            all_preds.extend(preds)
            all_labels.extend(labels[valid])
    return all_preds, all_labels


def evaluate_model(model, test_loader, save_path, model_name, tune_threshold=False):
    checkpoint = torch.load(save_path, map_location=DEVICE)
    # FIX: unwrap SWA AveragedModel if present
    target = model.module if hasattr(model, 'module') else model
    target.load_state_dict(checkpoint["model_state_dict"])
    print(f"Loaded checkpoint  epoch={checkpoint['epoch']}  "
          f"val F1={checkpoint['best_f1']:.4f}")

    if tune_threshold and model_name == "EfficientNet-Lite0":
        print("\n  🔧 Tuning threshold for Nutrient Deficiency...")
        from torch.utils.data import Subset
        val_ds = ClassificationDataset(val_df, transform=val_transform)
        val_subset = Subset(
            val_ds,
            np.random.choice(len(val_ds), min(200, len(val_ds)), replace=False)
        )
        val_tune_loader = DataLoader(val_subset, batch_size=BATCH_SIZE, shuffle=False)

        best_f1_tune = 0
        best_thresh  = 0.25
        for nd_thresh in [0.25, 0.22, 0.20, 0.18, 0.15, 0.12, 0.10]:
            thresholds = [0.25, 0.25, nd_thresh, 0.25]
            preds, labs = predict_with_thresholds(model, val_tune_loader, thresholds)
            m    = compute_metrics(preds, labs)
            nd_f1 = m["per_class_f1"].get("Nutrient Deficiency", 0)
            print(f"    ND thresh={nd_thresh:.3f} → "
                  f"ND F1={nd_f1:.4f}, overall F1={m['f1']:.4f}")
            if m['f1'] > best_f1_tune:
                best_f1_tune = m['f1']
                best_thresh  = nd_thresh

        print(f"  ✅ Best ND threshold: {best_thresh:.3f} (F1={best_f1_tune:.4f})")
        thresholds = [0.25, 0.25, best_thresh, 0.25]
        all_preds, all_labels = predict_with_thresholds(
            model, test_loader, thresholds)
    else:
        model.eval()
        all_preds, all_labels = [], []
        with torch.no_grad():
            for images, labels in tqdm(test_loader,
                                       desc=f"Testing {model_name}", leave=False):
                images = images.to(DEVICE)
                labels = labels.numpy()
                valid  = labels != -1
                if valid.sum() == 0:
                    continue
                with torch.amp.autocast("cuda"):
                    outputs = model(images[valid])
                all_preds.extend(torch.argmax(outputs, 1).cpu().numpy())
                all_labels.extend(labels[valid])

    metrics = compute_metrics(all_preds, all_labels, print_cm=False)

    print(f"\n── {model_name} — TEST RESULTS ──────────────────────────")
    print(f"  Accuracy                      : {metrics['accuracy']:.4f}")
    print(f"  Precision (weighted)          : {metrics['precision']:.4f}")
    print(f"  Recall    (weighted)          : {metrics['recall']:.4f}")
    print(f"  F1        (weighted)          : {metrics['f1']:.4f}")
    print(f"  FPR — Nutrient Deficiency     : {metrics['fpr_nutrient_def']:.4f}")

    print("\nPer-class F1:")
    for cls, f1v in metrics["per_class_f1"].items():
        status = "✅" if f1v >= 0.90 else "⚠️ "
        print(f"  {status} {cls}: {f1v:.4f}")

    print("\nClassification Report:")
    print(classification_report(
        metrics["all_labels"], metrics["all_preds"],
        labels=list(range(NUM_CLASSES)),
        target_names=list(CLASS_NAMES.values()), digits=4,
        zero_division=0))

    fig, ax = plt.subplots(figsize=(7, 6))
    short_names = [n.split()[0] for n in CLASS_NAMES.values()]
    sns.heatmap(metrics["confusion_matrix"], annot=True, fmt="d",
                cmap="Blues", ax=ax,
                xticklabels=short_names, yticklabels=short_names, cbar=True)
    ax.set_title(f"{model_name} — Confusion Matrix\n"
                 f"F1={metrics['f1']:.4f}  Acc={metrics['accuracy']:.4f}"
                 f"  FPR_ND={metrics['fpr_nutrient_def']:.4f}", fontsize=11)
    ax.set_ylabel("True"); ax.set_xlabel("Predicted")
    plt.tight_layout()
    cm_fname = os.path.join(
        META_DIR,
        f"confusion_matrix_{model_name.lower().replace(' ', '_')}.png")
    plt.savefig(cm_fname, dpi=120)
    plt.show()
    print(f"✅ Saved {cm_fname}")

    return metrics

