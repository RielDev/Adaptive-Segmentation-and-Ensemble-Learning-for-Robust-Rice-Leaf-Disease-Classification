"""**Single-Image Demo — Segmentation + Ensemble Prediction:**

Picks one random test image, runs the UNet, visualises the mask, runs the Mode B (segmentation) ensemble on it with the weights selected in Section 17, and prints the verdict against ground truth. Re-run the cell below for a different random sample.
"""

SEG_COLORS = {0: [0, 0, 0], 1: [255, 0, 0], 2: [255, 165, 0], 3: [0, 200, 0]}
SEG_LEGEND = {1: "Bacterial (red)", 2: "Brown spot (orange)", 3: "Nutrient def (green)", 0: "Background (black)"}

def seg_inference_single(unet, image_rgb_512, tta=SEG_TTA):
    def _fwd(img_np):
        aug = seg_transform(image=img_np)
        x = aug["image"].unsqueeze(0).to(DEVICE)
        with torch.no_grad(), torch.amp.autocast("cuda"):
            logit = unet(x)
        return torch.softmax(logit.squeeze(0), dim=0)

    probs = _fwd(image_rgb_512)
    if tta:
        hf = _fwd(np.fliplr(image_rgb_512).copy())
        probs = probs + torch.flip(hf, [2])
        vf = _fwd(np.flipud(image_rgb_512).copy())
        probs = probs + torch.flip(vf, [1])
        probs = probs / 3.0
    return probs.cpu()

def run_ensemble_demo(df, unet, classifiers, ensemble_weights, sample_idx=None):
    sample_idx = sample_idx if sample_idx is not None else random.randint(0, len(df) - 1)
    sample = df.iloc[sample_idx]
    img_path, true_label = sample["image_path"], int(sample["label"])

    image_bgr = cv2.imread(img_path)
    image_rgb = cv2.cvtColor(image_bgr, cv2.COLOR_BGR2RGB)
    image_512 = cv2.resize(image_rgb, (IMG_SIZE_SEG, IMG_SIZE_SEG))

    unet.eval()
    seg_probs = seg_inference_single(unet, image_512)

    fg_prob = 1.0 - seg_probs[0].numpy()
    weight_map = ((1.0 - SEG_ALPHA) + SEG_ALPHA * fg_prob)[:, :, np.newaxis]
    masked_512 = (image_512.astype(np.float32) * weight_map).clip(0, 255).astype(np.uint8)
    masked_display = cv2.resize(masked_512, (image_rgb.shape[1], image_rgb.shape[0]))

    seg_argmax = seg_probs.argmax(dim=0).numpy()
    seg_vis = np.zeros((512, 512, 3), dtype=np.uint8)
    for cls_idx, color in SEG_COLORS.items():
        seg_vis[seg_argmax == cls_idx] = color

    fig, axes = plt.subplots(1, 3, figsize=(15, 5))
    axes[0].imshow(image_rgb); axes[0].set_title("Original image"); axes[0].axis("off")
    axes[1].imshow(seg_vis); axes[1].set_title("UNet segmentation mask"); axes[1].axis("off")
    axes[2].imshow(masked_display); axes[2].set_title("Masked image (classifier input)"); axes[2].axis("off")
    fig.text(0.5, -0.02, "\n".join(SEG_LEGEND.values()), ha="center", fontsize=9, color="gray")
    plt.tight_layout(); plt.show()

    per_pixel_class = seg_argmax.flatten()
    pixel_counts = np.bincount(per_pixel_class, minlength=SEG_NUM_CLASSES)
    seg_to_cls = [3, 0, 1, 2]
    seg_prior = np.zeros(NUM_CLASSES, dtype=np.float32)
    for seg_idx, cls_idx in enumerate(seg_to_cls):
        seg_prior[cls_idx] = pixel_counts[seg_idx]
    seg_prior /= seg_prior.sum() + 1e-8

    input_tensor = val_transform(masked_512).unsqueeze(0).to(DEVICE)
    all_probs = []
    with torch.no_grad():
        for model in classifiers:
            logits = model(input_tensor)
            all_probs.append(torch.softmax(logits, dim=1).cpu().numpy()[0])

    ensemble_probs = np.zeros(NUM_CLASSES)
    for w, probs in zip(ensemble_weights, all_probs):
        ensemble_probs += w * probs
    pred_class = int(np.argmax(ensemble_probs))

    print("=" * 60)
    print("SEGMENTATION + ENSEMBLE PREDICTION")
    print("=" * 60)
    print(f"\nImage: {img_path}")
    print("\nUNet seg prior (mapped to classifier classes):")
    for i, name in CLASS_NAMES.items():
        print(f"  {name:<25}: {seg_prior[i]*100:.1f}%")
    print(f"\nPredicted Disease : {CLASS_NAMES[pred_class]}")
    print(f"Overall Confidence: {ensemble_probs[pred_class]*100:.2f}%")
    print("\nClass Probabilities (weighted ensemble):")
    for class_idx, class_name in CLASS_NAMES.items():
        bar = "#" * int(ensemble_probs[class_idx] * 30)
        print(f"  {class_name:<25}: {ensemble_probs[class_idx]*100:6.2f}%  {bar}")
    print(f"\nGround Truth: {CLASS_NAMES[true_label]}")
    print("\nResult:", "CORRECT" if pred_class == true_label else "INCORRECT")
    print("=" * 60)
    return pred_class, true_label

