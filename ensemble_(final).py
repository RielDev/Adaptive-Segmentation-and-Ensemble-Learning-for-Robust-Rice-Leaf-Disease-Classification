"""**Dataset — Mode B (Segmentation-Guided Images):**

For each image the UNet predicts a per-pixel softmax over 4 segmentation classes (`0=background, 1=bacterial, 2=brown_spot, 3=nutrient_def`). Two things are derived from that:

**(a) Background dimming (the actual classifier input).** Per-pixel foreground probability:
$$fg(p) = 1 - P_{seg}(\text{background} \mid p)$$
The image is reweighted pixel-by-pixel:
$$\text{weight}(p) = (1-\alpha) + \alpha \cdot fg(p), \qquad \text{masked}(p) = \text{image}(p) \cdot \text{weight}(p)$$
with $\alpha =$ `SEG_ALPHA` (0.35). $\alpha=0$ leaves the image untouched; $\alpha=1$ would zero out background entirely. The masked image is what actually gets resized to 224px and passed to the classifiers.

**(b) Segmentation prior (diagnostic only).** Take the arg-max segmentation class per pixel, count pixels per class, remap to the classifier's label space (`background→Healthy, bacterial→0, brown_spot→1, nutrient_def→2`), normalise to a distribution. This `seg_prior` is **not** fed into the classifiers — it's logged so Section 20 can sanity-check whether the UNet's own class guess agrees with ground truth.
"""

class SegmentedClassificationDataset(Dataset):
    """
    Returns (cls_image, label, seg_prior):
      cls_image : (3, IMG_SIZE_CLS, IMG_SIZE_CLS) -- seg-masked, normalised
      label     : int, 0-indexed
      seg_prior : (NUM_CLASSES,) float tensor -- UNet class distribution (diagnostic)
    """
    def __init__(self, df, seg_model, transform=None, alpha=SEG_ALPHA, tta=SEG_TTA):
        self.df, self.seg_model, self.transform = df.reset_index(drop=True), seg_model, transform
        self.alpha, self.tta = alpha, tta
        self._warned = set()

    def __len__(self):
        return len(self.df)

    def _seg_inference(self, image_rgb_512):
        """UNet (+ optional flip-TTA) on a (512,512,3) uint8 array -> softmax probs (4,512,512)."""
        def _fwd(img_np):
            aug = seg_transform(image=img_np)
            x = aug["image"].unsqueeze(0).to(DEVICE)
            with torch.no_grad(), torch.amp.autocast("cuda"):
                logit = self.seg_model(x)
            return torch.softmax(logit.squeeze(0), dim=0)

        probs = _fwd(image_rgb_512)
        if self.tta:
            hf = _fwd(np.fliplr(image_rgb_512).copy())
            probs = probs + torch.flip(hf, [2])
            vf = _fwd(np.flipud(image_rgb_512).copy())
            probs = probs + torch.flip(vf, [1])
            probs = probs / 3.0
        return probs.cpu()

    def _seg_prior(self, probs):
        per_pixel_class = probs.argmax(dim=0).numpy()
        pixel_counts = np.bincount(per_pixel_class.flatten(), minlength=SEG_NUM_CLASSES)
        seg_to_cls = [3, 0, 1, 2]   # bg->healthy, bacterial->0, brown_spot->1, nutrient->2
        prior = np.zeros(NUM_CLASSES, dtype=np.float32)
        for seg_idx, cls_idx in enumerate(seg_to_cls):
            prior[cls_idx] = pixel_counts[seg_idx]
        prior = prior / (prior.sum() + 1e-8)
        return torch.from_numpy(prior).float()

    def _apply_mask(self, image_rgb_512, probs):
        fg_prob = 1.0 - probs[0].numpy()
        weight = (1.0 - self.alpha) + self.alpha * fg_prob
        weight = weight[:, :, np.newaxis]
        masked = (image_rgb_512.astype(np.float32) * weight).clip(0, 255)
        return masked.astype(np.uint8)

    def __getitem__(self, idx):
        row, label = self.df.iloc[idx], int(self.df.iloc[idx]["label"])
        image_bgr = cv2.imread(row["image_path"])
        if image_bgr is None:
            if row["image_path"] not in self._warned:
                print(f"\nUnreadable: {row['image_path']}")
                self._warned.add(row["image_path"])
            dummy_prior = torch.zeros(NUM_CLASSES); dummy_prior[label] = 1.0
            return torch.zeros(3, IMG_SIZE_CLS, IMG_SIZE_CLS), -1, dummy_prior

        image_rgb = cv2.cvtColor(image_bgr, cv2.COLOR_BGR2RGB)
        image_512 = cv2.resize(image_rgb, (IMG_SIZE_SEG, IMG_SIZE_SEG))

        self.seg_model.eval()
        probs = self._seg_inference(image_512)
        seg_prior = self._seg_prior(probs)
        masked_512 = self._apply_mask(image_512, probs)

        cls_image = (self.transform or val_transform)(masked_512)
        return cls_image, label, seg_prior

