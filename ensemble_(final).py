"""**Ensemble Math — How the Final Probability Is Computed:**

This is the core logic everything below builds on.

Each of the $M=3$ classifiers (MobileNetV3, NASNet-Mobile, EfficientNet-Lite0) outputs raw logits $z_m(x) \in \mathbb{R}^4$ for an input image $x$.

**Step 1 — Per-model probabilities (softmax):**
$$p_{m,c}(x) = \frac{\exp(z_{m,c}(x))}{\sum_{c'=1}^{4} \exp(z_{m,c'}(x))}$$

**Step 2 — Weighted fusion across models:**
$$P_{ens,c}(x) = \sum_{m=1}^{M} w_m \, p_{m,c}(x), \qquad w_m \ge 0, \ \ \sum_{m=1}^{M} w_m = 1$$

**Step 3 — Final class prediction:**
$$\hat{y} = \arg\max_c \; P_{ens,c}(x)$$

The only unknowns are the $M$ weights $w_m$ — how much to trust each model. **Two independent methods** are used to find them, both fit on the **validation set only**; the test set is touched once per mode, purely for reporting.

**Weight-Finding Method 1 — Gradient-Based Optimisation:**

Weights must live on the probability simplex ($w_m \ge 0$, $\sum_m w_m = 1$). Rather than constrain the optimiser directly, we reparameterise: optimise unconstrained "raw" scores $\theta_m \in \mathbb{R}$ and pass them through a softmax to get valid weights:
$$w_m = \frac{\exp(\theta_m)}{\sum_{m'} \exp(\theta_{m'})}$$

`scipy.optimize.minimize` (L-BFGS-B) then searches $\theta$ to minimise the *negative* weighted F1 on the validation set:
$$\theta^{*} = \arg\min_{\theta} \; -F1_{weighted}\Big(\arg\max_c P_{ens,c}(x;\,w(\theta)) \;;\; y_{val}\Big)$$

**Caveat:** because the final `argmax` makes F1 a step function of the weights, the "gradient" L-BFGS-B follows is only a local, noisy approximation — it can stall in a flat region. That's why Method 2 exists as a cross-check.
"""

def weighted_ensemble_probs(logits_list, weights):
    """logits_list: list of (N,C) arrays, weights: (M,) array summing to 1 -> (N,C) probs."""
    probs = np.stack([softmax_np(l) for l in logits_list], axis=0)      # (M, N, C)
    w = np.array(weights)[:, np.newaxis, np.newaxis]                     # (M, 1, 1)
    return (probs * w).sum(axis=0)                                      # (N, C)

def neg_f1_objective(raw_weights, logits_list, true_labels):
    weights = np.exp(raw_weights) / np.exp(raw_weights).sum()
    ens_prob = weighted_ensemble_probs(logits_list, weights)
    preds = ens_prob.argmax(axis=1)
    return -f1_score(true_labels, preds, average="weighted", zero_division=0)

def optimise_weights(logits_list, true_labels, n_models, method=OPT_METHOD):
    """Finds w on the simplex that maximises weighted F1 via softmax-reparameterised L-BFGS-B."""
    x0 = np.zeros(n_models)
    result = minimize(neg_f1_objective, x0, args=(logits_list, true_labels),
                       method=method, options={"maxiter": OPT_MAX_ITER, "ftol": 1e-9})
    opt_weights = np.exp(result.x) / np.exp(result.x).sum()
    best_f1 = -result.fun
    return opt_weights, best_f1

print("optimise_weights() ready (Method 1 - gradient-based)")

"""**Weight-Finding Method 2 — Brute-Force Grid Search:**

With only $M=3$ models the simplex is 2-dimensional ($w_3 = 1-w_1-w_2$), so it's cheap to search exhaustively instead of trusting a local optimiser. We grid $(w_1, w_2)$ at step size `BRUTE_FORCE_STEP`, discard invalid combinations, and evaluate validation weighted-F1 directly for every point:
$$w^{*} = \arg\max_{w \,\in\, \text{grid}} \; F1_{weighted}\Big(\arg\max_c P_{ens,c}(x;w) \;;\; y_{val}\Big)$$
"""

def brute_force_weights(logits_list, true_labels, step=BRUTE_FORCE_STEP, verbose=False):
    best_f1, best_weights = -1, None
    weights_range = np.arange(0, 1.0 + step, step)

    for w1 in weights_range:
        for w2 in weights_range:
            w3 = 1 - w1 - w2
            if w3 < -1e-6 or w3 > 1 + 1e-6:
                continue
            weights = np.clip([w1, w2, w3], 0, 1)
            weights = weights / weights.sum()

            ens_prob = weighted_ensemble_probs(logits_list, weights)
            preds = ens_prob.argmax(axis=1)
            f1 = f1_score(true_labels, preds, average="weighted", zero_division=0)

            if f1 > best_f1:
                best_f1, best_weights = f1, weights
                if verbose:
                    print(f"  new best: w={np.round(weights, 3)}  F1={f1:.4f}")

    return best_weights, best_f1

print("brute_force_weights() ready (Method 2 - exhaustive grid search)")

